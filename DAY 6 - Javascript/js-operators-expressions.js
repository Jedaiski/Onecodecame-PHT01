//Arithmetic Operations
let x = 10;
let y = 5;

//sum '+'
let sum = x + y;
console.log("The sum is:", sum);

//difference '-'
let difference = x - y;
console.log("The difference is:", difference);

//product '*'
let product = x * y;
console.log("The product is:", product);

//quotient '/'
let quotient = x / y;
console.log("The quotient is:", quotient);

//Remainder or modulo
let remainder = x % 3;
console.log("The remainder:", remainder)

let answer = 10 + 2 * 23 / 2;
console.log("The answer is", answer);

//increment '++'
let increment = ++x;
console.log("The increment is:", increment);

//decrement '--'
let decrement = --y;
console.log("The decrement is:", decrement);

//Concatenation
let a = "Hello";
let b = "World!";
console.log(a + " " + b );

//Assignment expression
let i = 40;
let j = 20;

//Assignment expressiong '+='
i += j;
console.log(i);

//Assignment expressiong '-='
i -= j;
console.log(i);

//Assignment expressiong '*='
i *= j;
console.log(i);

//Assignment expressiong '/='
i /= j;
console.log(i);